class DatatypeClass
{
	public static void main(String [] args)
	{
		int x=2;
		byte y=1;
		long z;
		z=756566;
		short a=4;

		float b=2.3f;
		double c;
		c=4.567;

		boolean d=true;

		char e='r';

		System.out.println("\nbyte type variable: "+y+" ,short type variable: "+a+" ,int type variable: "+x+" ,long type variable: "+z);
		System.out.println("float type variable: "+b+" ,double type variable: "+c);
		System.out.println("boolean type variable: "+d);
		System.out.println("char type variable: "+e);	
	}	
}